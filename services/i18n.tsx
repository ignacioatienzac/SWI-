// ── Translation keys (flat dot-notation inferred at runtime) ─────────────────
// We use a plain Record so components just call t('section.key').

interface I18nContextType {
  lang: Language;
  setLang: (l: Language) => void;
  t: (key: string) => string;
  tArray: (key: string) => string[];
}

